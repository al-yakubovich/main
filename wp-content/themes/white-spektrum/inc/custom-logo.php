function white_spektrum_load_custom_logo() {
    /**
     ********************************************************************************************************************
     * add_theme_support( 'custom-logo', $args );. This feature allows you to setup a custom logo in the header if you 
     * choose do so.
     ********************************************************************************************************************
     */
    $args = array(
        'height'      => 100,
        'width'       => 400,
        'flex-height' => true,
        'flex-width'  => true,
        'header-text' => array( 'site-title', 'site-description' ),
    );
    add_theme_support( 'custom-logo', $args );
}
add_action( 'after_setup_theme', 'white_spektrum_load_custom_logo' );